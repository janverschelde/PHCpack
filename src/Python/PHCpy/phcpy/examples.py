    pol7 = "3*x3 + 2*x5 + x7 - 3.9701;"
    pol8 = "3*x1*x4 + 2*x1*x6 + x1*x8 - 1.7172;"
    pol9 = "3*x2*x4 + 2*x2*x6 + x2*x8 - 4.0616;"
